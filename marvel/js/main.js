let currentVideo = {
  "type" : "",
  "name" : "",
  "episode" : ""
} 

function play(type, name, episode=""){
  open("videoplayer.html")

  currentVideo.type = type
  currentVideo.name = name
  currentVideo.episode = episode 

  localStorage.setItem("currentVideo", JSON.stringify(currentVideo))
}

function loadVideo(){
  let videoplayer = document.getElementById("videoplayer")
  let video 

  currentVideo = JSON.parse(localStorage.getItem("currentVideo"))

  if (currentVideo.episode == "") {
    video = dict[currentVideo.type][currentVideo.name]
  } else {
    video = dict[currentVideo.type][currentVideo.name][currentVideo.episode]
  }
  
  videoplayer.setAttribute("src", video)
}

function setTvSerie(name){
  open("tv-series.html")
  localStorage.setItem("tvSerieName", JSON.stringify(name))
}

function loadTvSerie(){
  let name = JSON.parse(localStorage.getItem("tvSerieName"))
  let link = document.createElement("a")
  let text = document.createTextNode(name)
  link.appendChild(text)

  let element = document.getElementById("nome-serie")
  element.appendChild(link)

  link.setAttribute("class", "nav-link")

  name = name.replace(/ /g, "_")

  let list = document.createElement("li")

  for (let i in dict["serieTv"][name]){
    let list = document.createElement("li")
    let a = document.createElement("a")
    let text = document.createTextNode(i)
    a.appendChild(text)
    list.appendChild(a)

    let element = document.getElementById("elenco-episodi")
    element.append(list)
    let string = `play('serieTv', '${name}', '${i}')`
    a.setAttribute("onclick", string)
  }
}
